function range(start, end, step) { 
  var rangeArr =[];
  // if the step value exists
  if (step === undefined) {
    if (start === end) {
      return [];
    }
    else if (start < end) {
      for (var i = start; i <= end; i++) {
        rangeArr.push(i);
      }
    } else {
      for (var i = start; i >= end; i--) {
        rangeArr.push(i);
      }
    }
  } else {
    if (step < 0 && start < end) {
      return [];
    }
    else if (start < end) {
      for (var i = start; i <= end; i += step) {
        rangeArr.push(i);
      }
    } else {
      for (var i = start; i >= end; i += step) { // since adding neg numbs works like this
        rangeArr.push(i);
      }
    }
  }
  return rangeArr;
}

